const evidence_mod = @import("evidence.zig");

fn testCorpus(arena: std.mem.Allocator) !engine.Corpus {
    // 3 conformers + 1 rogue, built directly (no fs).
    var store = evidence_mod.Store.init(arena);
    var sets = std.ArrayList([]u32).init(arena);
    var artifacts = std.ArrayList(types.Artifact).init(arena);

    const shared = [_][]const u8{ "host=db", "port=5432", "tls=true" };
    const rogue = [_][]const u8{ "host=db", "backdoor=on", "debug=1" };

    const names = [_][]const u8{ "configs/a.yaml", "configs/b.yaml", "configs/c.yaml", "configs/rogue.yaml" };
    for (names, 0..) |name, aid| {
        var set = std.ArrayList(u32).init(arena);
        const prims: []const []const u8 = if (aid == 3) &rogue else &shared;
        for (prims, 0..) |c, line| {
            const r = try store.add(@intCast(aid), .{
                .kind = .kv,
                .canonical = c,
                .line = @intCast(line + 1),
            });
            if (r.first_for_artifact) try set.append(r.index);
        }
        try artifacts.append(.{ .id = @intCast(aid), .path = name, .kind = .yaml, .size = 100 });
        try sets.append(try set.toOwnedSlice());
    }

    const result = try analysis.analyze(arena, &store, 4, sets.items);
    const owned_sets = try sets.toOwnedSlice();
    const clusters = try @import("cluster.zig").detect(arena, &store, &result, owned_sets);
    return .{
        .artifacts = try artifacts.toOwnedSlice(),
        .store = store,
        .sets = owned_sets,
        .analysis = result,
        .clusters = clusters,
        .skipped = 0,
    };
}

test "evidence focuses on the artifact named in the question" {
    var arena_state = std.heap.ArenaAllocator.init(std.testing.allocator);
    defer arena_state.deinit();
    const arena = arena_state.allocator();

    const corpus = try testCorpus(arena);
    const ev = try buildEvidence(arena, &corpus, "why is rogue.yaml different?");

    try std.testing.expect(std.mem.indexOf(u8, ev, "FOCUS configs/rogue.yaml") != null);
    try std.testing.expect(std.mem.indexOf(u8, ev, "backdoor=on") != null);
    try std.testing.expect(std.mem.indexOf(u8, ev, "MISSING from this file") != null);
    try std.testing.expect(std.mem.indexOf(u8, ev, "port=5432") != null);
    // Conformers are not focus sections.
    try std.testing.expect(std.mem.indexOf(u8, ev, "FOCUS configs/a.yaml") == null);
}

test "evidence falls back to outliers when no artifact is named" {
    var arena_state = std.heap.ArenaAllocator.init(std.testing.allocator);
    defer arena_state.deinit();
    const arena = arena_state.allocator();

    const corpus = try testCorpus(arena);
    const ev = try buildEvidence(arena, &corpus, "what stands out here?");

    try std.testing.expect(std.mem.indexOf(u8, ev, "FOCUS configs/rogue.yaml") != null);
}

test "evidence building is deterministic" {
    var arena_state = std.heap.ArenaAllocator.init(std.testing.allocator);
    defer arena_state.deinit();
    const arena = arena_state.allocator();

    const corpus = try testCorpus(arena);
    const e1 = try buildEvidence(arena, &corpus, "why is rogue.yaml different?");
    const e2 = try buildEvidence(arena, &corpus, "why is rogue.yaml different?");
    try std.testing.expectEqualStrings(e1, e2);
}
